# Module: A module is a single Python file (.py) that contains Python code such as functions, classes, and variables.
# It can be imported into other scripts or modules.

# Example: math.py, os.py
# Package: A package is a directory that contains multiple modules and/or sub-packages.
# It must contain an __init__.py file (which can be empty) that tells Python the directory should be treated as a package.

# Example: mypackage/ which might contain multiple modules like module1.py, module2.py.